package entity;

import java.util.Date;

/**
 *
 * @author Foxxy
 */
public class ScheduleDate {
    private int id ;
    private Date date ;

    public ScheduleDate() {
    }
    
    public ScheduleDate(Date date) {
        this.date = date;
    }
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }
}
